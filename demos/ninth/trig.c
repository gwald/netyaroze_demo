int ratan (int x, int y)
{
	int angle = SUBratan(x, y);
	
	angle = 1024 - angle;

	if (angle < 0)
		angle += 2048;

	if (angle > 2048)
		angle -= 2048;

	return angle;
}



	// call with value as actual value * ONE
int ratan2 (int value)
{
	return ( ratan(ONE, value) );
}




	// Note: origin (x,z)=(0,1), clockwise rotation
	// this was originally done for atan on the x-z plane

int SUBratan (int x, int z)
{
  	if (x*z == 0)		  // if either is zero
    	return(ratan_zero(x,z));

  	if (x>0)
  		{
    	if (z>0)
    		{
      		x = abs(x);
      		z = abs(z);
      		if (x<=z)
				return(ArcTangentTable[255*x/z]);
      		else
				return(1024-ArcTangentTable[255*z/x]);
    		}
    	else
    		{
      		x = abs(x);
      		z = abs(z);
      		if (x<=z)
				return(2048-ArcTangentTable[255*x/z]);
      		else
				return(1024+ArcTangentTable[255*z/x]);
    		}
  		}
  	else
  		{
    	if (z>0)
    		{
      		x = abs(x);
      		z = abs(z);
      		if (x<=z)
				return(-ArcTangentTable[255*x/z]);
      		else
				return(-1024+ArcTangentTable[255*z/x]);
    		}
    	else
    		{
      		x = abs(x);
      		z = abs(z);
      		if (x<=z)
				return(-2048+ArcTangentTable[255*x/z]);
      		else
				return(-1024-ArcTangentTable[255*z/x]);
    		}
  		}
}




int ratan_zero (int x, int z)
{
  	if (x == 0)
	  	{
	    if(z >= 0)
	      	return(0);
	    else
	      	return(2048);
	  	}
  	else if (z == 0)
	  	{
	    if(x > 0)
	      	return(1024);
	    else
	      	return(-1024);
	  	}
	else
    	return(0);
}



long rinvtan(long x, long y);
int ratan3 (int opposite, int adjacent)
{
	int angle;

	if (adjacent == 0)
		return SILLY_VALUE;

	angle = rinvtan(adjacent, opposite);

	return angle;
}


long rinvtan(long x, long y)
{
	long t;

	if (x == 0 && y == 0)
		return 0;

	if (fabs(x) > fabs(y))
		{
		t = (y << 8) / x;

		if (t >= 0)
			return ArcTangentTable[t & 255] + (x < 0 ? 2048 : 0);

		return (x < 0 ? 2048 : 4096) - ArcTangentTable[(-t) & 255];
		}

	t = (x << 8) / y;

	if (t >= 0)
		return (y < 0 ? 3072 : 1024) - ArcTangentTable[t & 255];

	return ArcTangentTable[(-t) & 255] + (y < 0 ? 3072 : 1024);
}






	   





