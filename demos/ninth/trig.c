	// arcsin: first angle either 0-90 or 270-360
	// second is 90-270
void rasin (int value, int* smallerAngle, int* largerAngle)
{
	int tableIndex;
	int first, second;

		// sort range	-- seems to suggest a (arg is -ONE/2 to ONE/2)
		// VERY arbitrary; should be simpler for application to call ...
	while (value < -2048)
		value += 4096;
	while (value > 2048)
		value -= 4096;

	assert(value >= -2048);
	assert(value <= 2048);

	tableIndex = value + 2048;
	
	first = ArcSinTable[tableIndex]; 

	second = 2048 - first;

	if (first < 0)
		first += 4096;

	if (first >= second)
		{
		*largerAngle = first;
		*smallerAngle = second;
		}
	else
		{
		*largerAngle = second;
		*smallerAngle = first;
		}		
}


	// returns -90 to 90
int rasin2 (int value)
{
	int first, second;

	rasin(value, &first, &second);

	if (first >= -1024 && first	<= 1024)
		return first;
	else
		return (second - 4096);
}




	// expects second arg always positive, first can be anything
int rasin3 (int opposite, int hypoteneuse)
{
	int angle;
	int tableIndex;

	assert(hypoteneuse >= 0);
	assert(abs(opposite) <= hypoteneuse);

	if (hypoteneuse == 0)
		return SILLY_VALUE;

	tableIndex = 2048 + (2048 * opposite / hypoteneuse);

	assert(tableIndex >= 0 && tableIndex <= 4096);

	angle = ArcSinTable[tableIndex];

	return angle;
}


	// arg is -1 to 1, scaled up by ONE
int rasin4 (int value)
{
	int angle;
	int tableIndex;

	tableIndex = 2048 + (value / 2);

	assert(tableIndex >= 0 && tableIndex <= 4096);

	angle = ArcSinTable[tableIndex];

	return angle;
}



